# format spacifiers  =  {value : flags} format a value based on what 
#                                       flags are inserted

#  .(number)f  =  round to that many places/decimal points (fixed point)     ~ used this before .2f
#  :(number)   =  allocate that many characters of space
#  :03  =  allocate and zero pad that many spaces
#  :<   =  left justify
#  :>   =  right justify
#  :^   =  center aligned
#  :+   =  use plus sign to indicate positive value
#  :=   =  place sign to leftmost position
#  :    =  insert space before positive numbers
#  :,   =  comma as thousands separator


